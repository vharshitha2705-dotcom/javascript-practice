// Anonymous Function:
// A function without a name.
//
// The function is stored inside a variable.
// The variable name becomes the way to access it.

let sum1 = function(a, b) {

    return a + b
}

// Calling the function using the variable name
console.log(sum1(2, 6))

// Output:
// 8
