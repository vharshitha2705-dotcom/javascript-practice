// -------------------------------------
// replace() in JavaScript
// -------------------------------------

// replace() replaces the first occurrence
// of the specified value.

let sentence = "I like Java. Java is easy.";

console.log("Original String:", sentence);

let updatedSentence = sentence.replace("Java", "JavaScript");

console.log("Updated String:", updatedSentence);

// -------------------------------------
// replaceAll() in JavaScript
// -------------------------------------

// replaceAll() replaces all occurrences
// of the specified value.

let sentence = "I like Java. Java is easy.";

console.log("Original String:", sentence);

let updatedSentence = sentence.replaceAll("Java", "JavaScript");

console.log("Updated String:", updatedSentence);