// -------------------------------------
// toUpperCase() in JavaScript
// -------------------------------------

// Converts all characters in a string
// to uppercase.

let name = "Harshitha";

console.log("Original String:", name);

let upperCaseName = name.toUpperCase();

console.log("Uppercase String:", upperCaseName);

// -------------------------------------
// toLowerCase() in JavaScript
// -------------------------------------

// Converts all characters in a string
// to lowercase.

let name = "HARSHITHA";

console.log("Original String:", name);

let lowerCaseName = name.toLowerCase();

console.log("Lowercase String:", lowerCaseName);