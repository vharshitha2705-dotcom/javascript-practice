// ==================================================
// TRIM METHODS
// Used to remove extra spaces from strings.
// ==================================================

let name = "    Harshitha   "


// Total length including spaces
console.log(name.length)


// trim()
// Removes spaces from both beginning and end
console.log(name.trim().length)


// trimStart()
// Removes spaces only from the beginning
console.log(name.trimStart().length)


// trimEnd()
// Removes spaces only from the end
console.log(name.trimEnd().length)



