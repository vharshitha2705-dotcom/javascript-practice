// --------------------------------------------------
// Adding Elements
// --------------------------------------------------

var marks = [80, 70, 60, 50, 90, 70]

// push() adds an element to the end of the array
marks.push(78)

console.log(marks)

// length returns total number of elements
console.log(marks.length)


// unshift() adds an element at the beginning
// All existing elements shift one position right
marks.unshift(56)

console.log(marks)
console.log(marks.length)
