// --------------------------------------------------
// Searching in Arrays
// --------------------------------------------------

var marks = [80, 70, 60, 50, 90, 70]

// includes() checks whether an element exists
// Returns true or false

console.log(marks.includes(70)) // true
console.log(marks.includes(97)) // false


// --------------------------------------------------
// Finding Index Position
// --------------------------------------------------

// indexOf() returns the index of the first occurrence
// Returns -1 if the element is not found

console.log(marks.indexOf(60))
console.log(marks.indexOf(70))
console.log(marks.indexOf(95))
