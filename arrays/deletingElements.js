// --------------------------------------------------
// Removing Elements
// --------------------------------------------------

var marks = [80, 70, 60, 50, 90, 70]

// pop() removes the last element from the array
marks.pop()
console.log(marks)
console.log(marks.length)

// shift () removes the first element from the array
marks.shift()
console.log(marks)
console.log(marks.length)
